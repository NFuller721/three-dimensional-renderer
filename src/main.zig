pub fn drawModel(angle: f32) void {
    for (FACES) |face| {
        const p1 = VERTEXES[face[0] - 1].rotate_xz(angle).translate(0, 0, 25.00).project().viewport();
        const p2 = VERTEXES[face[1] - 1].rotate_xz(angle).translate(0, 0, 25.00).project().viewport();
        const p3 = VERTEXES[face[2] - 1].rotate_xz(angle).translate(0, 0, 25.00).project().viewport();

        raylib.drawLine(p1.x, p1.y, p2.x, p2.y, .green); 
        raylib.drawLine(p2.x, p2.y, p3.x, p3.y, .green); 
        raylib.drawLine(p3.x, p3.y, p1.x, p1.y, .green); 
    }
}

pub fn main() !void {
    raylib.initWindow(SCREEN_WIDTH, SCREEN_HEIGHT, WINDOW_TITLE);
    defer raylib.closeWindow();

    raylib.setTargetFPS(TARGET_FPS);

    var theta: f32 = 0.0;

    while (!raylib.windowShouldClose()) {
        raylib.beginDrawing();
        defer raylib.endDrawing();

        raylib.clearBackground(.white);
        drawModel(theta);

        theta += 0.01;
    }
}
